namespace Carfamsoft.Model2View.Shared
{
    /// <summary>
    /// Provides enumerated values for content alignment.
    /// </summary>
    public enum ContentAlignment
    {
        /// <summary>
        /// No content alignment.
        /// </summary>
        None,

        /// <summary>
        /// Align content at the top.
        /// </summary>
        Top,

        /// <summary>
        /// Align content to the right.
        /// </summary>
        Right,

        /// <summary>
        /// Align content at the bottom.
        /// </summary>
        Bottom,

        /// <summary>
        /// Align content to the left.
        /// </summary>
        Left,
    }
}
