namespace BlazorFormManager.Components
{
    /// <summary>
    /// Provides enumerated values for a component's position.
    /// </summary>
    public enum ComponentPosition
    {
        /// <summary>
        /// Places the component on the left of the viewport.
        /// </summary>
        Start,

        /// <summary>
        /// Places the component on the right of the viewport.
        /// </summary>
        End,

        /// <summary>
        /// Places the component on the top of the viewport.
        /// </summary>
        Top,

        /// <summary>
        /// Places the component on the bottom of the viewport.
        /// </summary>
        Bottom,
    }
}
